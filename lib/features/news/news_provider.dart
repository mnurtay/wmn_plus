            "Content-Type": "application/json",
            "Authorization": "wmn538179 ${user.result.token}",
          });
      String body = utf8.decode(response.bodyBytes);
      Map newsObject = json.decode(body);
      var newsList = AutogeneratedNews.fromJson(newsObject);
